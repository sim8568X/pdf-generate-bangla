পুলিশের মহাপরিদর্শক (আইজিপি) চৌধুরী আবদুল্লাহ আল মামুন আরাভ খানের বিষয়ে কথা বলছেনছবি: প্রথম আলো
আরাভ খানের সঙ্গে পুলিশের ঊর্ধ্বতন  সাবেক এক কর্মকর্তার সম্পর্কের বিষয়ে প্রশ্ন করা হলে আইজিপি বলেন, ‘পুলিশের ঊর্ধ্বতন একজনের কথা আমরা খতিয়ে দেখছি। উপযুক্ত সময়ে আমরা তা আপনাদের জানাব।’

আইজিপি আরও বলেন, ‘পুলিশ হত্যা মামলায় যে নামে চার্জশিট হয়েছে, সেই নামে রেড নোটিশ জারির জন্য ইন্টারপোলকে চিঠি পাঠানো হয়েছে। একটু আগে আমার কাছে খবর এসেছে ইন্টারপোল এটি গ্রহণ করেছে। এখন বাকি কাজ তারা করবে।’

দুবাইয়ের স্বর্ণ ব্যবসায়ী রবিউল ইসলাম ওরফে আরাভ খান (বাঁয়ে)। তাঁর গ্রামের বাড়িতে থাকেন না কেউ
দুবাইয়ের স্বর্ণ ব্যবসায়ী রবিউল ইসলাম ওরফে আরাভ খান (বাঁয়ে)। তাঁর গ্রামের বাড়িতে থাকেন না কেউপ্রথম আলো ফাইল ছবি
নায়িকা মাহিয়া মাহি সম্পর্কিত এক প্রশ্নের জবাবে চৌধুরী আবদুল্লাহ আল মামুন বলেন, ‘তিনি জামিন পেয়েছেন। আমি এটা বলতে পারি, কারও প্রতি কোনো অন্যায় করা হবে না।’

রোহিঙ্গা ক্যাম্পে সন্ত্রাসী কর্মকাণ্ড সম্পর্কিত এক প্রশ্নের জবাবে আইজিপি বলেন, ‘সম্পূর্ণরূপে আমাদের নিয়ন্ত্রণে রয়েছে। আমরা সবাই মিলে দায়িত্ব পালন করছি। পুলিশ, জেলা পুলিশ, এপিবিএন, র‍্যাব, বিভিন্ন গোয়েন্দা সংস্থা, প্রশাসন সবাই মিলে কাজ করছি। যাঁরা আইনশৃঙ্খলা বিঘ্নকারী  কাজ করছেন, তাঁদের প্রত্যেকের বিরুদ্ধে প্রতিটি ঘটনায় ব্যবস্থা নিচ্ছি। এমন নয় যে তাঁদের ছাড় দেওয়া হচ্ছে।’

