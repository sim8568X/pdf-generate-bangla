রবিউল ইসলাম ওরফে আরাভ
সংযুক্ত আরব আমিরাতের দুবাইয়ে গত বুধবার এক জমকালো অনুষ্ঠানের মধ্য দিয়ে আরাভ জুয়েলার্সের উদ্বোধন করেন আরাভ খান। উদ্বোধন উপলক্ষে ৬০ কেজি সোনা দিয়ে বানানো হয় বাজপাখির আদলে লোগো, যা তৈরিতে খরচ হয় প্রায় ৪৫ কোটি টাকা। ওই অনুষ্ঠানে বাংলাদেশের ক্রীড়া ও বিনোদনজগতের অনেক তারকাকে আমন্ত্রণ জানিয়ে ফেসবুকে একাধিক পোস্ট দিয়েছিলেন আরাভ খান। তখনই তাঁকে শনাক্ত করে ফেলেন আইনশৃঙ্খলা রক্ষাকারী বাহিনীর কর্মকর্তারা।

