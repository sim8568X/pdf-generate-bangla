<h2 style="text-align: center">আরাফ স্বর্ণ চোরাচালান</h2>
দুবাইয়ের আলোচিত স্বর্ণ ব্যবসায়ী রবিউল ইসলাম ওরফে আরাভ খানের সঙ্গে  পুলিশ ঊর্ধ্বতন সাবেক কোনো কর্মকর্তার সম্পর্কের বিষয়টি খতিয়ে দেখা হচ্ছে। পুলিশের মহাপরিদর্শক (আইজিপি) চৌধুরী আবদুল্লাহ আল মামুন এক প্রশ্নের উত্তরে এ কথা বলেছেন।

পুলিশ হত্যা মামলার আসামি আরাভের বিরুদ্ধে বাংলাদেশ থেকে পাঠানো রেড নোটিশ ইন্টারপোল গ্রহণ করেছে বলেও জানিয়েছেন আইজিপি।

আজ সোমবার দুপুরে চট্টগ্রাম মেট্রোপলিটন পুলিশের কোতোয়ালি থানার এনায়েত বাজার পুলিশ ফাঁড়ি উদ্বোধনের পর সাংবাদিকদের বিভিন্ন প্রশ্নের জবাবে তিনি এসব কথা বলেন।

